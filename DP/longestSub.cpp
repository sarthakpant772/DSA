class Solution{
public:
    int longestSubsequence(int N, int A[])
    {
        // code here
        vector<int> dp(10001,0);
        int ans=0;
        for(int i=0;i<N;++i){
            dp[A[i]]=max(dp[A[i]-1]+1,dp[A[i]+1]+1);
            ans=max(ans,dp[A[i]]);
        }
        
        return ans;
    }
};

